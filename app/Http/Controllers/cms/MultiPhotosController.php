    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $photo_array = [];

        $photos = $request->all();

        foreach($photos['file'] as $photo){
            array_push($photo_array, Photo::forModel($request->get('model_type'), $request->get('model_id'), $photo, true));
        }

        return response($photo_array);
    }

